var Firstnumber = 10;
var secondnumber = 5;
const operation = "/";

if (operation === "-") console.log(Firstnumber - secondnumber);
else if (operation === "+") console.log(Firstnumber + secondnumber);
else if (operation === "/") console.log(Firstnumber / secondnumber);
else if (operation === "*") console.log(Firstnumber * secondnumber);
